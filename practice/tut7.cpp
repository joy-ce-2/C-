//assignment operators and comparative and logical

#include <iostream>

using namespace std;

int main(){
    int a=2,b=3;
    cout<<"the value of a==b "<<(a==b);
    cout<<"the value of a!=b"<<(a!=b);
    cout<<"the value of a<=b"<<(a<=b);
    cout<<"the value of a>=b"<<(a>=b);
    cout<<"the value of a<b"<<(a<b);
    cout<<"the value of a>b"<<(a>b);
    return 0;

}
